    static void Para(int p) ///перевірка на парність
    {
        int parni = 0;
        int neparni = 0;


        for(int temp = p; temp != 0; temp /= 10) {
            if (temp % 2 == 0) {
                ++parni;
            } else {
                ++neparni;
            }
        }

        System.out.println("\nВикористовуючи десятковий запис цілочисельного значення кожної змінної знайти  кількість парних і непарних цифр:\nпарних: " + parni + "\nнепарних: " + neparni + "\nЧисло: " + p);
    }

    public static void NumbeOfUnits(String str )///Двійковий запис цілочисельного значення кожної змінної кількість одиниць
    {

        int count = 0;
        for(int  i = 0;i<str.length();i++) {
        	char ch1 = str.charAt(i);
            if (ch1 == '1') {
               ++count;
          }
      }

        System.out.println("\nДвійковий запис цілочисельного значення кожної змінної кількість одиниць: " + count + "\nЧисло у двійкові системі: " + str);
    }
